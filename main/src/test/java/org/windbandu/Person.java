package org.windbandu;

public class Person {
    private int age;
    private long secondsOnEarth;
    private int height;
    private String name;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public long getSecondsOnEarth() {
        return secondsOnEarth;
    }

    public void setSecondsOnEarth(long secondsOnEarth) {
        this.secondsOnEarth = secondsOnEarth;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
